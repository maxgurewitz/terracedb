use clap::Parser;
use std::{
    collections::BTreeSet,
    fmt::Write as _,
    fs, io,
    path::{Path, PathBuf},
};

const DENY_TESTS: &[&str] = &[];

#[derive(Debug, Parser)]
#[command(name = "generate_node_upstream_tests")]
struct Cli {
    #[arg(long)]
    repo_root: Option<PathBuf>,
    #[arg(long)]
    wrapper_path: Option<PathBuf>,
    #[arg(long)]
    body_path: Option<PathBuf>,
}

fn main() -> io::Result<()> {
    let cli = Cli::parse();
    let default_repo_root = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .ancestors()
        .nth(2)
        .expect("repo root")
        .to_path_buf();
    let repo_root = cli.repo_root.unwrap_or(default_repo_root);
    let tests_dir = repo_root.join("crates/terracedb-sandbox/tests");
    let generated_dir = tests_dir.join("generated_node_upstream");
    let wrapper_path = cli
        .wrapper_path
        .unwrap_or_else(|| generated_dir.join("main.rs"));
    let body_path = cli
        .body_path
        .unwrap_or_else(|| generated_dir.join("body.rs"));

    fs::create_dir_all(&generated_dir)?;

    let tests = collect_tests(&repo_root)?;
    fs::write(&wrapper_path, wrapper_source())?;
    fs::write(&body_path, body_source(&tests))?;

    println!("wrote {}", wrapper_path.display());
    println!("wrote {}", body_path.display());
    Ok(())
}

fn collect_tests(repo_root: &Path) -> io::Result<Vec<String>> {
    let deny = DENY_TESTS.iter().copied().collect::<BTreeSet<_>>();
    let mut tests = Vec::new();
    tests.extend(scan_suite_dir(
        &repo_root.join("third_party/node-src/test/parallel"),
        repo_root,
    )?);
    tests.extend(scan_suite_dir(
        &repo_root.join("third_party/node-src/test/sequential"),
        repo_root,
    )?);
    tests.sort();
    tests.retain(|path| {
        let node_prefixed = format!("/node{path}");
        !deny.contains(path.as_str()) && !deny.contains(node_prefixed.as_str())
    });
    Ok(tests)
}

fn scan_suite_dir(dir: &Path, repo_root: &Path) -> io::Result<Vec<String>> {
    let mut tests = Vec::new();
    for entry in fs::read_dir(dir)? {
        let entry = entry?;
        let path = entry.path();
        if !path.is_file() {
            continue;
        }
        let Some(name) = path.file_name().and_then(|value| value.to_str()) else {
            continue;
        };
        let is_relevant = (name.starts_with("test-module-") || name.starts_with("test-require-"))
            && name.ends_with(".js");
        if !is_relevant {
            continue;
        }
        let relative = path
            .strip_prefix(repo_root.join("third_party/node-src"))
            .expect("path under third_party/node-src");
        tests.push(format!("/{}", relative.display()));
    }
    tests.sort();
    Ok(tests)
}

fn wrapper_source() -> String {
    r#"// @generated by cargo run -p terracedb-sandbox --bin generate_node_upstream_tests
#[path = "../support/node_compat.rs"]
mod node_compat;

include!("body.rs");

fn format_case_timings(report: &terracedb_systemtest::SimulationSuiteReport) -> String {
    let mut output = String::new();
    for case in &report.cases {
        let status = match &case.status {
            terracedb_systemtest::SimulationCaseStatus::Passed => "passed".to_string(),
            terracedb_systemtest::SimulationCaseStatus::Failed { .. } => "failed".to_string(),
            terracedb_systemtest::SimulationCaseStatus::TimedOut { .. } => "timed_out".to_string(),
            terracedb_systemtest::SimulationCaseStatus::Panicked { .. } => "panicked".to_string(),
        };
        output.push_str(&format!(
            "[{}] {}: {:?} ({})\n",
            case.case_id, case.label, case.elapsed, status
        ));
    }
    output
}

#[tokio::test(flavor = "multi_thread")]
async fn generated_upstream_node_suite() {
    let started = std::time::Instant::now();
    let report = node_compat::run_generated_upstream_node_suite(GENERATED_UPSTREAM_NODE_CASES)
        .await
        .expect("run generated upstream node suite");
    let elapsed = started.elapsed();
    println!(
        "generated upstream node suite: {} in {:?}\n{}",
        report.summary_line(),
        elapsed,
        format_case_timings(&report)
    );
    assert!(
        report.is_success(),
        "generated upstream node suite: {} in {:?}\n{}\n{}",
        report.summary_line(),
        elapsed,
        format_case_timings(&report),
        report.failure_summary()
    );
}
"#
    .to_string()
}

fn body_source(tests: &[String]) -> String {
    let mut source = String::from(
        "// @generated by cargo run -p terracedb-sandbox --bin generate_node_upstream_tests\n\npub const GENERATED_UPSTREAM_NODE_CASES: &[node_compat::GeneratedUpstreamNodeCase] = &[\n",
    );
    for test_path in tests {
        let test_name = Path::new(test_path)
            .file_stem()
            .and_then(|value| value.to_str())
            .expect("test filename");
        writeln!(
            source,
            r#"    node_compat::GeneratedUpstreamNodeCase::new(
        "{test_name}",
        "{test_path}",
        30,
    ),
"#,
        )
        .expect("write generated test");
    }
    source.push_str("];\n");
    source
}
